package com.vuog.core.module.stream.domain.event;

/**
 * Enumeration of log levels
 */
public enum LogLevel {
    /**
     * Trace level - most detailed information
     */
    TRACE,

    /**
     * Debug level - debugging information
     */
    DEBUG,

    /**
     * Info level - general information
     */
    INFO,

    /**
     * Warn level - warnings
     */
    WARN,

    /**
     * Error level - errors
     */
    ERROR
}
